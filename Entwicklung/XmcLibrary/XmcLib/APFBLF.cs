        }


        internal static double GetGenderGBC(decimal Gewicht, string p)
        {
            Gewicht *= 1.00M;
            double gbc = 0.0;
            try
            {
                if (p == "m") { gbc = APF_Men_Dict[Gewicht]; }
                if (p == "w") {gbc =  APF_Women_Dict[Gewicht];}
                return gbc;
            }
            catch (KeyNotFoundException ex)
            {
                
              Console.WriteLine(
                  String.Format(
                  "Gewicht {0} für Gender {2} nicht in Tabelle:  {1}",
                        Gewicht, ex.Message, p));
                return 1.0;
            }
            
        }
        ///=================================================================================================
        /// <summary>   Rechne apf gewicht. </summary>
        ///
        /// <remarks>   Beate, 2/27/2013. </remarks>
        ///
        /// <param name="d">                The decimal to process. </param>
        /// <param name="searchLowFakt">    [out] The search low fakt. </param>
        /// <param name="searchHighFakt">   [out] The search high fakt. </param>
        ///-------------------------------------------------------------------------------------------------

        static void rechneAPFGewicht(decimal d, out decimal searchLowFakt, out decimal searchHighFakt)
        {


            string vorkomma = string.Empty;
            string nachkomma = string.Empty;
            string nachkommaH = string.Empty;  // von 90,83  - die 8
            string nachkommaZ = string.Empty;  // von 90,83  - die 3
            string nkLow = string.Empty;       // bei 90,83  - 80 

            string nkMid = string.Empty;       // bei 90,83  - 85  decimal gewMid = 0;                // wird      90,85
            string nkHigh = string.Empty;      // bei 90,83  - 90   (nklow + 10)



            string s = d.ToString();      // zB s = "99,34" 
            string[] a = s.Split(',');
            vorkomma = a[0];

            if (a.Length == 1)
            {
                nachkomma = "00";
                nachkommaH = "0";
                nachkommaZ = "0";

            }


            if (a.Length == 2 && a[1].Length == 1) // nur eine Stelle nach dem Komma oder n,n0 angegeben ( zB 78,30 )
            { // erweitern auf 2 Stellen
                nachkomma = a[1] + "0";
                nachkommaH = a[1];
                nachkommaZ = "0";
            }
            else
            {
                if (a.Length == 2)
                {
                    nachkomma = a[1];
                    nachkommaH = a[1].Substring(0, 1);
                    nachkommaZ = a[1].Substring(1, 1);
                }
            }


            BerechneGewichteLMH(d, nachkommaH, nachkommaZ, vorkomma,
                out searchLowFakt, out searchHighFakt);

        }


        private static void BerechneGewichteLMH(decimal d, string nachkommaH,
           string nachkommaZ, string vorkomma,
           out decimal gewFaktLow, out decimal gewFaktHigh)
        {
            string vkKorr = "0";
            decimal gewLow = 0;
            decimal gewMid = 0;
            decimal gewHigh = 0;
            decimal gewBody = d;

            gewFaktLow = 0;
            gewFaktHigh = 0;

            if (nachkommaH == "0" && nachkommaZ == "0")
            {
                // low = mid = high
                gewLow = Convert.ToDecimal(d);
                gewMid = gewLow;
                gewHigh = gewLow;
                gewFaktLow = gewBody;
                gewFaktHigh = gewBody;
                return;
            }



            if (nachkommaH.Equals("9"))
            {
                int vk = Convert.ToInt32(vorkomma) + 1;
                vkKorr = vk.ToString();
            }

            string nkLow = nachkommaH + "0";
            string nkMid = nachkommaH + "5";
            string nkHigh = string.Empty;
            int tmp = Convert.ToInt32(nachkommaH) + 1;
            if (vkKorr.Equals("0"))
            {
                nkHigh = tmp.ToString() + "0";
                vkKorr = vorkomma;
            }
            else
            {

                nkHigh = "00";
            }
            string gew = vorkomma + "," + nkLow;
            gewLow = Convert.ToDecimal(gew);
            gew = vorkomma + "," + nkMid;
            gewMid = Convert.ToDecimal(gew);
            gew = vkKorr + "," + nkHigh;
            gewHigh = Convert.ToDecimal(gew);

            if (gewBody == gewLow || gewBody == gewMid || gewBody == gewHigh)
            {
                gewFaktLow = gewBody;
                gewFaktHigh = gewBody;
            }

            if (gewBody > gewLow && gewBody < gewMid)
            {
                gewFaktLow = gewLow;
                gewFaktHigh = gewMid;
            }
            if (gewBody > gewMid && gewBody < gewHigh)
            {
                gewFaktLow = gewMid;
                gewFaktHigh = gewHigh;

            }

        }

