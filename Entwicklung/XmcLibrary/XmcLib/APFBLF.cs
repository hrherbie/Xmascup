        public static double GetGBC(double Gewicht, string Gender)
        {
            double GBC = 0;
            double lowGBC = 0;
            double highGBC = 0;

            decimal searchLowFakt;
            decimal searchHighFakt;

            decimal decGew = Convert.ToDecimal(Gewicht);
            rechneAPFGewicht(decGew,
                out  searchLowFakt, out  searchHighFakt);
            
                lowGBC = GetGenderGBC(searchLowFakt, Gender);
                highGBC = GetGenderGBC(searchHighFakt, Gender);
